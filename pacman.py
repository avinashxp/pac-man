def square(x, y):
    """Draw square using path at (x, y)."""
    path = document.getElementById('pacman-canvas').getContext('2d')
    path.fillStyle = 'blue'
    path.fillRect(x, y, 20, 20)

def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Draw world using path."""
    canvas = document.getElementById('pacman-canvas')
    ctx = canvas.getContext('2d')
    ctx.fillStyle = 'black'
    ctx.fillRect(0, 0, canvas.width, canvas.height)
    ctx.fillStyle = 'blue'

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                ctx.fillStyle = 'white'
                ctx.beginPath()
                ctx.arc(x + 10, y + 10, 2, 0, 2 * Math.PI)
                ctx.fill()

def move():
    """Move pacman and all ghosts."""
    writer = document.getElementById('score')
    writer.textContent = state['score']

    canvas = document.getElementById('pacman-canvas')
    ctx = canvas.getContext('2d')
    ctx.clearRect(0, 0, canvas.width, canvas.height)

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    ctx.fillStyle = 'yellow'
    ctx.beginPath()
    ctx.arc(pacman.x + 10, pacman.y + 10, 10, 0, 2 * Math.PI)
    ctx.fill()

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        ctx.fillStyle = 'red'
        ctx.beginPath()
        ctx.arc(point.x + 10, point.y + 10, 10, 0, 2 * Math.PI)
        ctx.fill()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    window.setTimeout(move, 100)

def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

